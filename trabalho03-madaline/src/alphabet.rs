/// Converte bitmap 0/1 para valores bipolares -1.0/+1.0
pub fn to_bipolar(bitmap: &[[u8; 7]; 9]) -> Vec<f64> {
    bitmap.iter().flatten().map(|&b| if b == 1 { 1.0 } else { -1.0 }).collect()
}

/// Gera amostras de treino: base + variantes com ruído determinístico
pub fn gerar_amostras(n_variantes: usize) -> Vec<(Vec<f64>, usize)> {
    let mut amostras = Vec::new();
    for (idx, (_, bitmap)) in LETRAS.iter().enumerate() {
        let base = to_bipolar(bitmap);
        amostras.push((base.clone(), idx));
        for v in 0..n_variantes {
            let mut variante = base.clone();
            let p1 = (idx * 7 + v * 13 + 5) % 63;
            let p2 = (idx * 11 + v * 17 + 31) % 63;
            variante[p1] *= -1.0;
            variante[p2] *= -1.0;
            amostras.push((variante, idx));
        }
    }
    amostras
}
